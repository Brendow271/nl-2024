square = ->(x) { x ** 2 } # Лямбда, которая возводит в квадрат

# Метод с блоком, который применяет итератор 'map' к массиву
def process(arr)
  arr.map { |n| yield(n) } # применяет блок ко всем элементам массива
end

nums = [1, 2, 3, 4, 5]
result = process(nums, &square)

puts result.inspect  # [1, 4, 9, 16, 25]
